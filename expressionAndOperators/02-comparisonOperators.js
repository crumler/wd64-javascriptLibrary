// Equal

'3' == 3;
console.log('3' == 3);

/*

 - JavaScript is helpful and does something known as coercion when comparing values
 - Coercion is the process of converting a value from one type to another

*/

//Strict Equal
console.log("3" === 3);

// === is strictly equal

console.log('Bob' === 'Bob');
console.log('Bob' === 'bob');

//Not Equal
console.log('35' != 3);

//Strict Not Equal
console.log('3' !== 3);

//Greater Than

3 > 2;

//Less Than

3 < 2;

//Greater Than or Equal to

3 >= 2;

//Less Than or Equal to

3 <= 2;

//And

2 && 3;  //checks to see the values of these two items are the same (think This & That)

//Or
2 || 3;