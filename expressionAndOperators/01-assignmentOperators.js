//ASSIGNMENT OPERATOR:
let r = 5;
//the equal sign is the operator here

//ADDITION OPERATOR:

r += 1;

//SUBTRACTION OPERATOR:

r -= 1;
console.log(r);

//MULTIPLICATION OPERATOR:

r *= 3;
console.log(r);

//DIVISION OPERATOR:

r /= 2;
console.log(r);

//EXPONENTIAL OPERATOR:

r**=2;
console.log(r);

//REMAINDER

r %= 1;
console.log(r);